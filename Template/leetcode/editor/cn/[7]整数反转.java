class Solution {
    public int reverse(int x) {
        int res = 0;
        // max = 2147483647
        // min = -2147483648
        double max = Math.pow(2, 31) - 1, min = -Math.pow(2, 31);
        while (x != 0) {
            int temp = x % 10;
            // å¦‚æœå‰9ä½å·²ç»å¤§äºmaxçš„å‰ä¹ä½|| å‰9ä½ç­‰äºmaxçš„å‰ä¹ä½ï¼Œæœ€åä¸€ä½å¤§äºmaxçš„æœ€åä¸€ä½
            // åˆ™æ²¡å¿…è¦ç»§ç»­è®¡ç®—ï¼Œç›´æ¥åˆ¤æ–­æº¢å‡º
            if (res > max / 10 || (res == max / 10 && temp > 7))
                return 0;
            // å¦‚æœå‰9ä½å·²ç»å°äºminçš„å‰ä¹ä½|| å‰9ä½ç­‰äºminçš„å‰ä¹ä½ï¼Œæœ€åä¸€ä½å¤§äºmaxçš„æœ€åä¸€ä½
            if (res < min / 10 || (res == min / 10 && temp > 8))
                return 0;
            x = x / 10;
            res = res * 10 + temp;
        }
        return res;
    }
}
